int	ft_is_prime(int nb)
{
	int	i;
	int	p;

	p = 0;
	i = 1;
	while (i <= nb)
	{
		if (nb % i == 0)
		{
			p++;
		}
		i++;
	}
	if (p == 2)
	{
		return (1);
	}
	else
	{
		return (0);
	}
}
